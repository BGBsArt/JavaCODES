class MonApp
{
    public static void main(String[] args)
    {
        System.out.println(" Ekaaro, c'est mon premier programme.\n");

        final int DATE = 02;  // 'final' nous permet de définir une constante. DATE est alors une constante
                            // On ne peut modifier ni faire varier son contenu.

        // Utilisons la constante DATE pour afficher des dates
        System.out.println(" Aujourd'hui est le " + DATE + " Décembre 2023 !");
        System.out.println(" Le " + DATE + " Janvier 2024, fera exactement un mois. Une nouvelle année.");
    }
}